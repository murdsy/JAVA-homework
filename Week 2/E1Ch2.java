/*
 * Description: Excercise 1, chapter 2
 * WHat is the numeric value of each of the following expressions as evaluated by JAVA?
 */

public class E1Ch2 {
    public static void main(String[] arg){
        System.out.println("a) "+ 4+6*2); //412
        System.out.println("b) "+ 10/5+8); //28
        System.out.println("c) "+ 12/4+16/2); //38
        System.out.println("d) "+ 17/2); //8
        System.out.println("e) "+ 22/5);//4
        System.out.println("f) "+ 39/10); //3
        System.out.println("g) "+ 19%(2+3)); //4
        System.out.println("h) "+ 3+4*20/3); //326
        System.out.println("i) "+ 36%(6+2)); //4
        System.out.println("j) "+ 8%2*0); //0

    }
    
}
